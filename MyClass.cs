// MyClass class
public class MyClass {

	private string mName;
	private int mID;

	// MyClass constructor
	public MyClass(){

	}

	// Name property
	public string Name {
		get {
			return mName;
		}
		set {
			mName = value;
		}
	}

	// ID property
	public int ID {
		get {
			return mID;
		}
		set {
			mID = value;
		}
	}
}